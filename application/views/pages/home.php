            <p>
                The introduction of Web 2.0 moved the software development platform from the desktop to the browser <a href="#">(O'Reilly, 2005)</a>. Asynchronous JavaScript and XML (AJAX) led to the development of Rich Internet Applications (RIAs), or web applications that behave like desktop applications <a href="#">(Garrett, 2005)</a>. This movement was accompanied with an increase in both commercial and academic offerings for web-based code editors, which promise to make code generation and testing viable across different browsers and operating platforms.</p>
            <p>The current web development tools range from these web-based code editors to What You See Is What You Get (WYSIWYG) website builders. WYSIWYG emphasizes dragging and dropping elements onto a web page.Both of these have their advantages, but compromise in either generating code or style. Web-based code editors lack the traditional drag-and-drop interface common to desktop code editors. WYSIWYG editors are extremely limited in their support for JavaScript and data storage. Software that combines both manual coding and WYSIWYG, such as Adobe Dreamweaver, often result in bloated and unreadable code.</p>
